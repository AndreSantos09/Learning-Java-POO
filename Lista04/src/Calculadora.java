public class Calculadora {

    // methods to calculate
    public int soma(int a, int b) {
        int result = a + b;
        System.out.println("A soma = " + result);
        return result;
    }

    public double soma(Double a, Double b) {
        double result = a + b;
        System.out.println("A soma = " + result);
        return result;

    }

}
